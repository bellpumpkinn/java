package ch05.home.ex03.case1;

public class Main {
	public static void main(String[] args) {
		Cat cat = new Cat(); //실행시 고양이 객체가 1마리가 생긴다.
		
		cat.setColor("검은"); //색깔은 속성명, 검은은 속성값
		cat.setName("야코미"); // 이름은 속성명 야코미는 속성값
		//이름을 지어줬습니다를 setName으로 표현.
	}
}
/*
과제] 검은 고양이 한마리를 주웠습니다.
야코미라고 이름을 지어줬습니다.
*/